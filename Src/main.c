  /* USER CODE BEGIN 2 */
  HAL_Delay(10000);
  NRF24_begin(NRF_CE_GPIO_Port, NRF_CS_Pin, NRF_CE_Pin, hspi1);
  printRadioSettings();
  
  //**** TRANSMIT - ACK ****//
  NRF24_stopListening();
  NRF24_openWritingPipe(TxpipeAddrs);
  NRF24_setAutoAck(true);
  NRF24_setChannel(52);
